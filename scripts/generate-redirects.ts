/**
 * Build-time generator for public/_redirects.
 *
 * Every /vs/[a]-vs-[b] page is rendered at its canonical (alphabetical)
 * slug. To avoid orphaning the inverse URL (a real concern for press,
 * vendor copy, and external backlinks that might write the pair in
 * either order), we emit a 301 from the non-canonical inverse to the
 * canonical. Cloudflare Pages reads _redirects natively.
 */
import fs from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";
import {
  listAllVsPairs,
  comparisonSlug,
} from "../src/lib/comparisons.ts";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const PUBLIC_DIR = path.join(__dirname, "..", "public");

async function main() {
  const pairs = await listAllVsPairs();
  const lines: string[] = [
    "# Auto-generated by scripts/generate-redirects.ts — do not edit.",
    "# Sourced from every /vs/* pair that has appeared in any cycle.",
    "",
    "# Vs-page inverse → canonical (alphabetical) redirects",
  ];
  let count = 0;
  for (const pair of pairs) {
    const canonical = comparisonSlug([pair.a, pair.b]); // alphabetical
    const inverse = `${pair.b}-vs-${pair.a}`; // non-alphabetical (always different from canonical)
    if (inverse === canonical) continue; // safety; shouldn't ever happen
    lines.push(`/vs/${inverse} /vs/${canonical} 301`);
    count++;
  }
  await fs.mkdir(PUBLIC_DIR, { recursive: true });
  await fs.writeFile(
    path.join(PUBLIC_DIR, "_redirects"),
    lines.join("\n") + "\n",
  );
  console.log(`[redirects] wrote ${count} vs-inverse redirects`);
}

main().catch((err) => {
  console.error("[redirects] failed:", err);
  process.exit(1);
});
